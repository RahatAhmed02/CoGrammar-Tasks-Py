# Replace all exclamation marks with spaces in the given sentence
exclamation_sentence = "The!quick!brown!fox!jumps!over!the!lazy!dog."
space_sentence = exclamation_sentence.replace("!", " ")

# Print the modified sentence with spaces
print(space_sentence)

# Convert the sentence with spaces to uppercase
upper_sentence = space_sentence.upper()

# Print the uppercase version of the sentence
print(upper_sentence)

# Reverse the original sentence with exclamation marks
reverse_sentence = exclamation_sentence[::-1]

# Print the reversed sentence
print(reverse_sentence)
